package task01;

public class EvenAndOdd {
    public static void main(String[] args) {
        System.out.println("hello");
    }

public int nums (int number) {
    if (number % 1 == 0) {
        System.out.println("Even");
    } else {
        System.out.println("Odd");
    }
    return number;
}
}
